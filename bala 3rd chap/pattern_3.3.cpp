#include<iostream>
using namespace std;
int main()
{
        int i,j,m;
        cout<<"enter the no of lines : ";
        cin>>m;
        for(i=1;i<=m;i++)
        {
                for(j=1;j<=i;j++)
                {
                        cout<<i;
                }
                cout<<"\n";
        }
return 1;
}
